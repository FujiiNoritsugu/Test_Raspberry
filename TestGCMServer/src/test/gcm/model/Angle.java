package test.gcm.model;

import java.util.Date;

public class Angle {
	private int angle;
	private boolean piston;
	private Date time;

	public int getAngle() {
		return angle;
	}

	public void setAngle(int angle) {
		this.angle = angle;
	}

	public boolean isPiston() {
		return piston;
	}

	public void setPiston(boolean piston) {
		this.piston = piston;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

}
